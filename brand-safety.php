      <p>Affectv utilises both direct publisher relationships and buying platforms (exchanges and SSPs) to discover audiences in real-time. In order to find these audiences optimally and achieve the reach needed, Affectv buys media across a
        wide range of websites. However as the safety of our client’s brand is of utmost value, Affectv have several layers of control in place to make sure we deliver ads in the right environments for each campaign.</p>

