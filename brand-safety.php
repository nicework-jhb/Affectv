      <h2 class="text-purple">Ad-<span class="font-var"><em>Verification</em></span></h2>

      <p>Affectv uses third party ad-verification technology (Adloox) across all campaigns to ensure accurate and efficient ad-exposure. While this technology is used to provide ad-visibility measures for brand campaigns, the referrer data
        is also utilised for both placement and performance verification, and is included in the above quality checks.</p>
      <ul>
        <li>Adloox will remove ads from displaying that are on URLs detected inappropriate, ensuring the user cannot see the ad or click through to the landing page.</li>
        <li>Affectv will then reactively block these domains. Additionally, we are able to work with most ad verification technologies that a client/agency may use.</li>
      </ul>
      <p>Buying Platform Controls Affectv utilises DSP platforms and Ad Exchanges to access some of our inventory. The following platform-level controls ensure brand safety for clients:</p>
      <ul>
        <li>Platform audited inventory only. We do not access inventory that has not gone through a platform’s own audits.</li>
        <li>Sensitive attributes. We do not run on any sites that contain sensitive attributes apart from, where requested by clients, Moderated Forums, Social Media and Photo/Video Sharing platforms.</li>
      </ul>

      <h2 class="text-purple" id="domain-lists">Domain <span class="font-var"><em>Lists</em></span></h2>

      <h3>Blacklists</h3>
      <ul>
        <li>Affectv uses blacklists to block specific URLs across all buying activity. This list currently has tens of thousands of domains and includes the following categories: Abortion, Adult, Aggressive, Guns, Hacking, Violence, Adware,
          Malware, Drugs, and Fraud. We have several programs monitoring the sites from where we receive traffic and updating these blacklists, in addition to having manual checks and reviews in place.
        </li>
        <li>Affectv also has several other specialised blacklists that are added to campaigns. These include: LOL and Image sites, non-premium dating sites, gif sites, video sharing, and anime sites.</li>
        <li>If there are specific domains that a client does not want to run on, Affectv will blacklist them.</li>
      </ul>
      <h3>Whitelists</h3>
      <ul>
        <li>For brands where tight control of sites is critical, Affectv will work to a whitelist of sites that has been agreed upon by Affectv and the client. Ads will only appear on those domains, whether through direct publisher buys or
          through buy platforms (domain list bidding only). Affectv will include assurance, as stated upon the signed IO, that activity will only run on the agreed whitelist. Please note, this may have an impact in terms of the number of
          users we are able to reach.
        </li>
      </ul>

      <h2 class="text-purple">Internal <span class="font-var"><em>Audits</em></span></h2>

      <p>Affectv has both manual and automated processes to continually monitor inventory quality.</p>
      <ul>
        <li>Affectv runs weekly checks on all the URLs that have impressions served against them, for both direct and platform-bought activity. These URLs are referenced against the blacklist, a keyword list and domain prefix lists. Sites
          that reference against any of these are manually checked, and those that are deemed inappropriate are added to the blacklists.
        </li>
        <li>Placement performance is manually monitored by Campaign Managers. Individual placements that heavily over-index or under-index against historic performance benchmarks (CTR%, CR%) are included on a watch list and are investigated
          for fraudulent activity (click-bots, hidden iframes, pop-under activity, forced clicks etc).
        </li>
        <li>Domains that indicate impression and click fraud, or low visibility, are automatically added to a blacklist to prevent low performance.</li>
      </ul>
      <h2 class="text-purple">Direct <span class="font-var"><em>Publisher Activity</em></span></h2>

      <p>All direct sites that Affectv work with are sorted and categorised based on the audit principles outlined by DTSG. For brand campaigns, all direct sites will be included in whitelists agreed between the client and Affectv (see:
        <a href="#domain-lists">Domain lists</a>).</p>

      <h2 class="text-purple">Take <span class="font-var"><em>Down Policy</em></span></h2>

      <p>Affectv endeavors to the highest quality of delivery, by focusing on high quality inventory combined with the right targeting. If any misplaced ads are detected by any of our vendors who monitor brand safety, they are
        over-delivered free of cost. Affectv have weekly proactive internal checks that are automated as well as manual to avoid misplacement. Our clients also can inform us to avoid specific sites which are actioned on ASAP, and requests
        to pause activity are actioned within 2 hours. As standard, Affectv has a 48 hour termination clause.</p>

      <p>The contractual consequences of not taking reasonable steps to remove an ad from a publication on written request are evaluated and agreed with the buyer on a case by case basis.</p>
