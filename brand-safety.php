      <p>Affectv is also a signatory to the Digital Trading Standards Group’s (DTSG) Good Practice Principles (lease see <a href="http://www.jicwebs.org/agreed-principles/digital-trading-standards-group-good-practice-principles/151-dtsgsignatories">JIC Webs Agreed Principles</a>).<br>
